package com.buania.buanialaxtechhblux.services;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.mail.SimpleMailMessage;
import org.springframework.mail.javamail.JavaMailSender;
import org.springframework.stereotype.Service;


@Service
public class HBLuxServices {

    @Autowired
    private JavaMailSender javaMailSender;

    public void sendMailToSomeOne(String from,String to,String body,String topic){
        System.out.println("Pret à envoyer");
        SimpleMailMessage simpleMailMessage=new SimpleMailMessage();
        simpleMailMessage.setFrom(from);
        simpleMailMessage.setText(body);
        simpleMailMessage.setTo(to);
        simpleMailMessage.setSubject(topic);
        System.out.println(simpleMailMessage);
        javaMailSender.send(simpleMailMessage);
        System.out.println("Bien envoyé");
    }
}
